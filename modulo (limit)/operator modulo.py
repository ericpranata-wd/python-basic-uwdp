# operator modulo
# disimbolkan dengan %
# berfungsi untuk mengambil sisa pembagian
# contoh 

# 5*2**-1 akan menjadi 2 sisa 1
# dan 1 akan menjadi output

print (5%2)
# output akan menjadi 1
print (8%3)
# output akan menjadi 2
print (7//3)
# 7/3 == 2 sisa 1
# jika menggunakan (//) maka 2 akan menjadi output
print (11/3)
# hasil biasa
print (11//3)
# desimal
print (11%3)
# sisa