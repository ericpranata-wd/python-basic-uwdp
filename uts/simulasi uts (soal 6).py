r=float(input('Masukan jari-jari silinder   : '))
t=float(input('Masukan tinggi silinder      : '))

print(f' Volume silinder adalah {3.14*r*r*t}')
print(f' Luas silinder adalah {2*3.14*r(r+t)}')
print(f' Keliling silinder adalah {(4*(3.14*r))+(2*t)}')
print(f' Diagonal terpanjang silinder adalah {((2*r)**2+t**2)**1/2}')